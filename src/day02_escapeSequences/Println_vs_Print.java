package day02_escapeSequences;

public class Println_vs_Print {

    public static void main(String[] args){

        //'println' mean you hit enter, and it will be printed on next line
        System.out.println("Hello!");
        System.out.println("This is Ali");

        System.out.println("~~~~~~~~~~~~~");

    /*  'print' mean it will print on same line
        doesn't matter how many print statement you write
    */
        System.out.print("Hello!");
        System.out.print("This is Hamza");


    }


}
