package day02_escapeSequences;

public class EscapeSequences {

    public static void main(String[] args){

        // use \n in same statement to make new line
        System.out.println("Hello!This is me");
        System.out.println("Hello!\nThis is me");
        System.out.println("----------------------------------------------------------------");
        // use \t to make paragraph space in lines. could be start, middle, anywhere
        System.out.println("Name Hamza");
        System.out.println("\tName\tHamza");
        System.out.println("----------------------------------------------------------------");
        // To use backslash'\' in statement you have to use double backslash'\\'
        System.out.println("\\");
        System.out.println("----------------------------------------------------------------");
        // to use double quote(") in the statement use \" in start and end
        System.out.println("\"I am Human\"");



    }



}
/*
1. \n: new line
2. \t: tab Space
3. \\: Single Backslash
4. \": Double Quote

 */