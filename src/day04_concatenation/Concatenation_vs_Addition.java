package day04_concatenation;

public class Concatenation_vs_Addition {
    public static void main(String [] args){

        System.out.println("32" + 1);//concatenation
        System.out.println(1 + "21");//concatenation
        System.out.println("12" + "34");//concatenation
        System.out.println("--------------------------------------");

        System.out.println(12 + 2.5);// addition
        System.out.println(3 + 43);
    }
}
