package day04_concatenation;

public class ArithmeticOperators {
    public static void main(String[] args) {

        System.out.println(20 + 6);// addition

        System.out.println(20 - 6);// subtraction

        System.out.println(20 / 6);// int result means no decimal // 3
        System.out.println(20 / 6d); // put (d) on either side to get the decimal result
        System.out.println(20d / 6);
        System.out.println(20d / 6d); // d stands for double
        System.out.println(20.0 / 6); // or put .0 on either side
        System.out.println( (int) (2.5 / 0.5) );// to get int number u can change the type

        System.out.println(20 * 6);// multiplication

        System.out.println(20 % 6);// remainder
    }

}
