package day03_variables;

public class VariableNamingRules {
    public static void main(String[] args) {

        //age is 23
        // income is 12000

        int age = 23; // always use variable names that make sense
        int income = 12000; // always use name that is related to data
        //int age = 32; // variable name must be UNIQUE or can't use the same name u used before

        int phonenumber = 1234567890;// without camelcase
        double salaryBeforeTax = 12000.5;//always use CamelCase
        //double salary before tax = 12000.5;// never use space in variable name
        // you can use only _ and $ in variable names.

        System.out.println(age);
        System.out.println(income);
        System.out.println(phonenumber);
        System.out.println(salaryBeforeTax);

    }


}
